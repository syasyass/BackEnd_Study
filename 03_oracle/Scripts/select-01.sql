SELECT ENAME, SAL, SAL*12+COMM, COMM
FROM emp; -- 테이블 명

SELECT ENAME, SAL, SAL*12+COMM AS ANNSAL, COMM -- 수식의 열에 별칭 부여. 공백이 포함되면 ""로 둘러싸기.
FROM emp; -- 테이블 명

SELECT * FROM EMP
ORDER BY SAL; -- 오름차순 정렬

SELECT * FROM EMP
ORDER BY SAL DESC; -- 내림차순 정렬

SELECT * FROM EMP
ORDER BY DEPTNO ASC, SAL DESC; -- DEPTNO로 오름차순 정렬, 동률이면 SAL로 내림차순 정렬

